package bean;

public class Getterbean {
	//如果是管理员发布的信息，失主取回时需填写下方信息
	public String get_name;
	public String get_site;
	public String get_time;
	public String get_status;
	public String get_tel;
	
	public String getGet_name() {
		return get_name;
	}
	public void setGet_name(String get_name) {
		this.get_name = get_name;
	}
	public String getGet_site() {
		return get_site;
	}
	public void setGet_site(String get_site) {
		this.get_site = get_site;
	}
	public String getGet_time() {
		return get_time;
	}
	public void setGet_time(String get_time) {
		this.get_time = get_time;
	}
	public String getGet_status() {
		return get_status;
	}
	public void setGet_status(String get_status) {
		this.get_status = get_status;
	}
	public String getGet_tel() {
		return get_tel;
	}
	public void setGet_tel(String get_tel) {
		this.get_tel = get_tel;
	}
	
	
}
